public class Robot {

	private String head;
	private String tail;
	private String arm;
	private String leg;

	public void setHead(String h) {
		head = h;
	}

	public String getHead() {
		return head;
	}

	public void setTail(String t) {
		tail = t;
	}

	public String getTail() {
		return tail;
	}

	public void setArm(String a) {
		arm = a;
	}

	public String getArm() {
		return arm;
	}

	public void setLeg(String l) {
		leg = l;
	}

	public String getLeg() {
		return leg;
	}
}



